public class AsciiValue   
{  
public static void main(String[] args)   
{   
char ch1 = 'g';  
char ch2 = 'k';  
int ascii1 = (int) ch1;  
int ascii2 = (int) ch2;  
System.out.println("The ASCII value of " + ch1 + " is: " + ascii1);  
System.out.println("The ASCII value of " + ch2 + " is: " + ascii2);  
}  
}